package assignments;

public class Pyramid01 {

	public static void main(String[] args) {

		int i, j;
        for(i=0; i<5; i++)
        {
            for(j=0; j<=i; j++)
            {
                System.out.print("* ");
            } // end of for loop
            System.out.println();
        } // end of for loop
		
	} // end of main()
} // end of Pyramid01 class
